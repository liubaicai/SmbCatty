export const getUiThemeById = (theme: "light" | "dark", id: string): UiThemePreset => {
  const list = theme === "dark" ? DARK_UI_THEMES : LIGHT_UI_THEMES;
  const found = list.find((preset) => preset.id === id);
  return found || list[0];
};
